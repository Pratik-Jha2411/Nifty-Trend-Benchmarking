# Set plotting style
sns.set_style('whitegrid')

def fetch_data(ticker="^CRSLDX"):
    """
    Fetch historical data for the given ticker.
    Tries the primary ticker first, then falls back to ^NSEI if empty.
    """
    print(f"Fetching data for {ticker}...")
    try:
        df = yf.download(ticker, start="2010-01-01")
        
        if df.empty:
            print(f"Data for {ticker} is empty. Trying fallback ticker ^NSEI (Nifty 50).")
            ticker = "^NSEI"
            df = yf.download(ticker, start="2010-01-01")
        
        # Check if MultiIndex columns (Ticker, Price) and flatten
        if isinstance(df.columns, pd.MultiIndex):
            df.columns = df.columns.get_level_values(0)

        print(f"Data fetched successfully. Rows: {len(df)}")
        return df

    except Exception as e:
        print(f"Error fetching data: {e}")
        return None

def add_technical_indicators(df):
    """
    Calculate and add technical indicators to the dataframe.
    """
    df = df.copy()
    
    # Simple Moving Averages
    df['SMA_10'] = ta.trend.sma_indicator(df['Close'], window=10)
    df['SMA_50'] = ta.trend.sma_indicator(df['Close'], window=50)

    # Exponential Moving Averages
    df['EMA_10'] = ta.trend.ema_indicator(df['Close'], window=10)
    df['EMA_50'] = ta.trend.ema_indicator(df['Close'], window=50)

    # RSI
    df['RSI'] = ta.momentum.rsi(df['Close'], window=14)

    # MACD
    df['MACD'] = ta.trend.macd_diff(df['Close'])

    # Bollinger Bands
    df['BB_High'] = ta.volatility.bollinger_hband(df['Close'])
    df['BB_Low'] = ta.volatility.bollinger_lband(df['Close'])

    # Average True Range
    df['ATR'] = ta.volatility.average_true_range(df['High'], df['Low'], df['Close'])

    # Drop NaN values generated by indicators
    df.dropna(inplace=True)
    
    return df

def prepare_data(df):
    """
    Prepare features (X) and target (y) for modeling.
    Target: 1 if Close[t] > Close[t-1], else 0 (Up/Down)
    Features: Uses T-1 data to predict T direction using shift(1).
    """
    df = df.copy()
    
    # Create Target (Up/Down)
    df['Target'] = np.where(df['Close'] > df['Close'].shift(1), 1, 0)

    # Define feature columns
    feature_cols = ['Open', 'High', 'Low', 'Close', 'Volume', 
                    'SMA_10', 'SMA_50', 'EMA_10', 'EMA_50', 
                    'RSI', 'MACD', 'BB_High', 'BB_Low', 'ATR']
    
    # Shift features to align T-1 features with T target
    X = df[feature_cols].shift(1)
    y = df['Target']

    # Remove the first row (NaN due to shift)
    X = X.iloc[1:]
    y = y.iloc[1:]
    
    return X, y

def evaluate_models(X, y):
    """
    Train and evaluate multiple models using Walk-Forward Validation.
    """
    models = {
        'Logistic Regression': LogisticRegression(max_iter=1000),
        'Polynomial Logistic Regression': Pipeline([
            ('poly', PolynomialFeatures(degree=2)),
            ('log_reg', LogisticRegression(max_iter=1000))
        ]),
        'Ridge Classifier': RidgeClassifier(),
        'Lasso Logistic Regression': LogisticRegression(penalty='l1', solver='liblinear', max_iter=1000),
        'ElasticNet Logistic Regression': LogisticRegression(penalty='elasticnet', solver='saga', l1_ratio=0.5, max_iter=1000),
        'Decision Tree': DecisionTreeClassifier(random_state=42),
        'Random Forest': RandomForestClassifier(n_estimators=100, random_state=42),
        'SVM': SVC(random_state=42),
        'XGBoost': XGBClassifier(use_label_encoder=False, eval_metric='logloss', random_state=42)
    }

    # Walk-Forward Validation
    tscv = TimeSeriesSplit(n_splits=5)
    results = []

    print("Starting evaluation...")

    for name, model in models.items():
        print(f"Evaluating {name}...")
        accuracies = []
        precisions = []
        recalls = []
        f1s = []
        
        for train_index, test_index in tscv.split(X):
            X_train, X_test = X.iloc[train_index], X.iloc[test_index]
            y_train, y_test = y.iloc[train_index], y.iloc[test_index]
            
            # Scaling
            scaler = StandardScaler()
            X_train_scaled = scaler.fit_transform(X_train)
            X_test_scaled = scaler.transform(X_test)
            
            try:
                model.fit(X_train_scaled, y_train)
                y_pred = model.predict(X_test_scaled)
                
                accuracies.append(accuracy_score(y_test, y_pred))
                precisions.append(precision_score(y_test, y_pred, zero_division=0))
                recalls.append(recall_score(y_test, y_pred, zero_division=0))
                f1s.append(f1_score(y_test, y_pred, zero_division=0))
                
            except Exception as e:
                print(f"Error training {name}: {e}")
                accuracies.append(np.nan)

        # Average results
        results.append({
            'Model': name,
            'Accuracy': np.mean(accuracies),
            'Precision': np.mean(precisions),
            'Recall': np.mean(recalls),
            'F1 Score': np.mean(f1s)
        })

    return pd.DataFrame(results)

